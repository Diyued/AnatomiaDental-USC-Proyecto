// Selecciona todos los dientes con la clase "tooth" (esto puede depender del ID, así que los seleccionamos por ID)
const teeth = document.querySelectorAll('area');

// Recorre cada diente y añade los eventos de click y mouseover/mouseout
teeth.forEach(tooth => {
    // Evento para mostrar el carrusel al hacer clic en el diente
    tooth.addEventListener('click', () => {
        showImageCarousel(tooth.id);
    });

    // Evento para mostrar el número del diente al hacer mouseover
    tooth.addEventListener('mouseover', (event) => {
        const numeros = document.getElementById('numero');
        numeros.textContent = `Diente ${tooth.id.replace('tooth', '')}`; // Actualiza el número del diente
        numeros.style.display = 'block';
        numeros.style.left = `${event.pageX + 10}px`;
        numeros.style.top = `${event.pageY + 10}px`;
    });

    // Evento para ocultar el número del diente al hacer mouseout
    tooth.addEventListener('mouseout', () => {
        const numeros = document.getElementById('numero');
        numeros.style.display = 'none';
    });
});

// Función para mostrar el carrusel de imágenes basado en el diente seleccionado
function showImageCarousel(dienteId) {
    const carrusel = document.getElementById('carrusel');
    const diente = dientesInfo[dienteId]; // Obtiene la información del diente

    if (diente) {
        // Genera dinámicamente las imágenes del diente
        carrusel.innerHTML = `
        <p>${diente.description}</p> <!-- Muestra la descripción del diente -->
        `;

        carrusel.innerHTML += diente.images.map((image, index) => `
    <img src="${image}" alt="Imagen ${index + 1}" style="display: ${index === 0 ? 'inline' : 'none'}; width: 400px; height: 400px;">
`).join('');


        carrusel.innerHTML += `
            <div class="button-container">
                <button id="prev">Anterior</button>
                <button id="next">Siguiente</button>
                <button id="atras">Atras</button>
            </div>
            
        `;

        const prevButton = document.getElementById('prev');
        const nextButton = document.getElementById('next');
        const backButton = document.getElementById('atras');

        let currentImageIndex = 0;
        const images = carrusel.querySelectorAll('img');

        function showImage(index) {
            images.forEach((img, i) => {
                img.style.display = i === index ? 'inline' : 'none';
            });
        }

        prevButton.addEventListener('click', () => {
            currentImageIndex = currentImageIndex > 0 ? currentImageIndex - 1 : images.length - 1;
            showImage(currentImageIndex);
        });

        nextButton.addEventListener('click', () => {
            currentImageIndex = (currentImageIndex + 1) % images.length;
            showImage(currentImageIndex);
        });

        // Al presionar "Atrás", volvemos a la pantalla de la dentadura
        backButton.addEventListener('click', () => {
            window.location.reload(); // Esto recarga la página actual
        });
    }
}
